export const ITALIAN_PROVINCE_SET = new Set(ITALIAN_PROVINCE_SIGLE);

export const PROVINCE_LEGACY_PREFIX = "__legacy:";

export function provinceFieldValue(stored: string): string {
  const raw = stored.trim();
  if (!raw) return "";
  const upper = raw.toUpperCase();
  if (ITALIAN_PROVINCE_SET.has(upper)) return upper;
  return `${PROVINCE_LEGACY_PREFIX}${raw}`;
}

export function provinceFieldOnChange(rawValue: string): string {
  if (!rawValue || rawValue.startsWith(PROVINCE_LEGACY_PREFIX)) return "";
  return rawValue;
}
